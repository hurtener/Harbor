/**
 * The injectable Protocol-client interface. A page depends on THIS interface,
 * never on the concrete `HarborClient` — tests and the Playwright harness
 * inject a deterministic in-page implementation (CONVENTIONS.md §6).
 */
export interface ProtocolClient {
	readonly tools: ToolsNamespace;
	readonly memory: MemoryNamespace;
	readonly flows: FlowsNamespace;
	readonly artifacts: ArtifactsNamespace;
	readonly mcp: MCPNamespace;
	readonly events: EventsNamespace;
}

/**
 * The unified production Protocol client. ONE class, ONE `fetch` choke point
 * (the shared {@link Transport}), method namespaces hanging off it. Constructed
 * once from a resolved {@link RuntimeConnection}; immutable after construction.
 *
 * A page resolves its connection via `connection.ts`, constructs a
 * `HarborClient`, and calls `client.<namespace>.<method>(...)`. New page
 * surfaces add a namespace here — never a new top-level client.
 */
export class HarborClient implements ProtocolClient {
	readonly tools: ToolsNamespace;
	readonly memory: MemoryNamespace;
	readonly flows: FlowsNamespace;
	readonly artifacts: ArtifactsNamespace;
	readonly mcp: MCPNamespace;
	readonly events: EventsNamespace;

	constructor(opts: HarborClientOptions) {
		const transport = new Transport(opts);
		this.tools = new ToolsNamespace(transport);
		this.memory = new MemoryNamespace(transport);
		this.flows = new FlowsNamespace(transport);
		this.artifacts = new ArtifactsNamespace(transport);
		this.mcp = new MCPNamespace(transport);
		this.events = new EventsNamespace(transport);
	}
}
