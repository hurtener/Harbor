/**
 * The `control.*` namespace — the shipped Phase 54 task-control surface
 * (`cancel` / `pause` / `resume` / `prioritize` / `approve` / `reject`
 * + `redirect` / `inject_context` / `user_message`). The Runtime mounts
 * these on the control surface at `POST /v1/control/{verb}`.
 *
 * The Console Tasks-page bulk-action toolbar + per-task action bar
 * consume THESE shipped verbs — no new control method is minted
 * (CLAUDE.md §13). A verb targets a specific run by its task id carried
 * in `identity.run`; the optional `payload` carries method-specific
 * arguments (the `priority` for `prioritize`).
 */
export class ControlNamespace {
	readonly #t: Transport;
	constructor(t: Transport) {
		this.#t = t;
	}
	/**
	 * Dispatch a Phase 54 control verb against the run identified by
	 * `taskID` (carried in `identity.run`). `payload` carries the
	 * method-specific arguments; `scope` is the caller's steering scope
	 * claim. Returns the {@link ProtocolError}-on-failure ControlResponse.
	 */
	dispatch<R = unknown>(
		verb: string,
		taskID: string,
		payload?: Record<string, unknown>,
		scope = 'owner_user'
	): Promise<R> {
		const body: Record<string, unknown> = {
			identity: { run: taskID, scope }
		};
		if (payload !== undefined) {
			body.payload = payload;
		}
		return this.#t.request<R>(`/v1/control/${verb}`, body);
	}
	/**
	 * `start` — spawn a new foreground task in the caller's session.
	 *
	 * Round-6 F7 — `start` is NOT a steering verb. The wire shape is
	 * FLAT (mirrors `internal/protocol/types/control.go::StartRequest`):
	 * `{identity:triple, query, description, priority, idempotency_key}`,
	 * with no `task:{...}` wrapper. The previous Playground sendMessage
	 * went through `dispatch('user_message', sessionID, ...)` which (a)
	 * treated sessionID as a taskID and (b) used the steering-verb body
	 * shape — the runtime rejected it with "no live run for the
	 * requested run id" and the chat panel could never send its first
	 * message.
	 *
	 * The runtime folds the connection-scoped identity (tenant/user/
	 * session) into `identity` automatically via {@link Transport.request}
	 * — we do NOT override identity here. Kind is hardcoded to
	 * `foreground` at the dispatch layer (background runs go through
	 * `tasks.spawn_background`, not `start`).
	 */
	start<R = { task_id: string; reused: boolean; protocol_version: string }>(
		query: string,
		opts: {
			description?: string;
			priority?: number;
			idempotencyKey?: string;
			inputArtifactIDs?: string[];
		} = {}
	): Promise<R> {
		const body: Record<string, unknown> = { query };
		if (opts.description !== undefined) {
			body.description = opts.description;
		}
		if (opts.priority !== undefined) {
			body.priority = opts.priority;
		}
		if (opts.idempotencyKey !== undefined) {
			body.idempotency_key = opts.idempotencyKey;
		}
		// Round-7 F11 / D-166 — multimodal artifact inputs. The runtime
		// resolves each id to a `planner.InputArtifactView` and routes
		// per MIME on the first planner turn: image/* bytes inline as
		// `ImagePart.DataURL`; pdf/audio stay as typed refs; everything
		// else emits an `ArtifactStub` text block the LLM routes via
		// the tool catalog. Empty / undefined keeps the wire shape
		// text-only (the omitempty tag elides the field server-side).
		if (opts.inputArtifactIDs !== undefined && opts.inputArtifactIDs.length > 0) {
			body.input_artifact_ids = opts.inputArtifactIDs;
		}
		return this.#t.request<R>('/v1/control/start', body);
	}
	/** `cancel` — cancel the task's run. */
	cancel<R = unknown>(taskID: string): Promise<R> {
		return this.dispatch<R>('cancel', taskID);
	}
	/** `pause` — pause the task's run. */
	pause<R = unknown>(taskID: string): Promise<R> {
		return this.dispatch<R>('pause', taskID);
	}
	/** `resume` — resume the task's run. */
	resume<R = unknown>(taskID: string): Promise<R> {
		return this.dispatch<R>('resume', taskID);
	}
	/** `prioritize` — set the task's numeric priority. */
	prioritize<R = unknown>(taskID: string, priority: number): Promise<R> {
		return this.dispatch<R>('prioritize', taskID, { priority });
	}
	/** `approve` — approve a pending HITL gate on the task's run. */
	approve<R = unknown>(taskID: string): Promise<R> {
		return this.dispatch<R>('approve', taskID);
	}
	/** `reject` — reject a pending HITL gate on the task's run. */
	reject<R = unknown>(taskID: string): Promise<R> {
		return this.dispatch<R>('reject', taskID);
	}
}

