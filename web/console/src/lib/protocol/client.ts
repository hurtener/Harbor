// Harbor Console — the unified typed Protocol client (D-121,
// CONVENTIONS.md §6).
//
// The foundation audit found five hand-authored Protocol clients —
// `ToolsClient`, `MemoryClient`, `FlowsClient`, the `mcpApi` object, and
// `HTTPProtocolClient` — each with its own error class, its own
// identity-passing convention, and its own `fetch` choke point. This module
// unifies them onto ONE `HarborClient` class with method namespaces hanging
// off it (`client.tools.list(...)`, `client.memory.health(...)`, …), one
// `ProtocolError`, and ONE transport choke point.
//
// `.svelte` components NEVER call `fetch` directly (CLAUDE.md §4.5 rule 5,
// §13). They construct a `HarborClient` (or receive an injected
// `ProtocolClient`) and call its typed namespace methods.
//
// # Routes
//
// The Runtime does not serve a single uniform endpoint shape. Each namespace
// method targets whatever route the Runtime actually mounts for it —
// `tools.*` at `POST /v1/tools/{verb}`, `memory.*` at `POST /v1/memory/{verb}`,
// `flows.*` at `POST /v1/flows/...`, `artifacts.*` and `mcp.servers.*` at the
// control surface `POST /v1/control/{method}` (CONVENTIONS.md §6).
//
// # Deviation note (D-093)
//
// D-093 pins `src/lib/protocol.ts` as generated by `cmd/harbor-gen-protocol-ts`.
// That generator binary has not yet landed (Phase 72h committed `protocol.ts`
// as a hand-shaped stub; the five legacy per-page clients are likewise hand-
// authored). This module is the hand-authored unified client; when the
// generator lands, the wire types fold into the generated file and this client
// re-exports them — a mechanical migration.

