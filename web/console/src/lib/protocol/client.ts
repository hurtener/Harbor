/** The HTTP verb a route is mounted under. All Protocol routes are POST. */
type Method = 'POST';

/** Options the `HarborClient` is constructed with. */
export interface HarborClientOptions {
	/** The resolved Runtime connection (base URL + token + identity triple). */
	connection: RuntimeConnection;
	/**
	 * Injectable `fetch` implementation. Defaults to `globalThis.fetch`; tests
	 * and the Playwright harness inject a deterministic stub so the client is
	 * exercised without a live Runtime.
	 */
	fetchImpl?: typeof fetch;
}

/**
 * Transport is the single `fetch` choke point shared by every namespace. It
 * serialises the request, folds the identity triple into the body (the
 * Runtime's defence-in-depth identity check), attaches the bearer token + the
 * `X-Harbor-*` identity headers, and raises a typed {@link ProtocolError} on
 * any non-2xx response — never a silently-swallowed empty result (CLAUDE.md
 * §13 "no silent degradation").
 */
export class Transport {
	readonly #baseURL: string;
	readonly #token: string;
	readonly #identity: RuntimeConnection['identity'];
	readonly #fetch: typeof fetch;

	constructor(opts: HarborClientOptions) {
		this.#baseURL = opts.connection.baseURL.replace(/\/$/, '');
		this.#token = opts.connection.token;
		this.#identity = opts.connection.identity;
		this.#fetch = opts.fetchImpl ?? globalThis.fetch.bind(globalThis);
	}

	/** The identity triple this transport is scoped to (read-only). */
	get identity(): RuntimeConnection['identity'] {
		return this.#identity;
	}

	/**
	 * The Runtime base URL (no trailing slash). Exposed so the
	 * `events.subscribe` SSE consumer can construct an `EventSource`
	 * target — the SSE stream is a long-lived `GET` that the request /
	 * response `Transport.request` choke point does not model. The SSE
	 * consumer is still a member of the `events` namespace, not a
	 * hand-rolled per-page client (CONVENTIONS.md §6).
	 */
	get baseURL(): string {
		return this.#baseURL;
	}

	/** The bearer JWT this transport carries (read-only). */
	get token(): string {
		return this.#token;
	}

	/**
	 * Issue one typed request against a Runtime route. `path` is the route the
	 * Runtime mounts (e.g. `/v1/tools/list`, `/v1/control/artifacts.list`).
	 * The identity triple is folded into the body; `body` is merged over it so
	 * a caller-supplied `identity` field wins for cross-tenant admin calls.
	 */
	async request<T>(path: string, body: Record<string, unknown> = {}, method: Method = 'POST'): Promise<T> {
		const payload = JSON.stringify({ identity: this.#identity, ...body });
		const resp = await this.#fetch(`${this.#baseURL}${path}`, {
			method,
			headers: {
				'Content-Type': 'application/json',
				Authorization: `Bearer ${this.#token}`,
				'X-Harbor-Tenant': this.#identity.tenant,
				'X-Harbor-User': this.#identity.user,
				'X-Harbor-Session': this.#identity.session
			},
			body: payload
		});
		const text = await resp.text();
		if (!resp.ok) {
			let code = 'runtime_error';
			let message = text || resp.statusText || `HTTP ${resp.status}`;
			try {
				const err = JSON.parse(text) as Partial<ProtocolErrorBody>;
				if (err.code) code = err.code;
				if (err.message) message = err.message;
			} catch {
				// Non-JSON error body — keep the raw text as the message; never
				// swallow the failure (CLAUDE.md §5 fail-loudly).
			}
			throw new ProtocolError(code, message, resp.status);
		}
		return (text.length > 0 ? JSON.parse(text) : {}) as T;
	}
}

